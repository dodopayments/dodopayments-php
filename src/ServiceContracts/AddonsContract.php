     *
     * @throws APIException
     */
    public function update(
        string $id,
        string|Currency|null $currency = null,
        ?string $description = null,
        ?string $imageID = null,
        ?string $name = null,
        ?int $price = null,
        string|TaxCategory|null $taxCategory = null,
        ?RequestOptions $requestOptions = null,
    ): AddonResponse;

    /**
